import mongoose from "mongoose";

const userSchema = new mongoose.Schema({
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  first_name: {
    type: String,
    required: true,
  },
  last_name: {
    type: String,
    required: true,
  },
  group: {
    type: String,
    required: true,
  },
});

const GoogleUser = mongoose.model("GoogleUser", userSchema);
export default GoogleUser;
